<?php

class Mathematics {
    static $pi = 3.14;

    public static function addition($a, $b) {
        return $a + $b;
    }

    public static function subtraction($a, $b) {
        return $a - $b;
    }

    public static function multiplication($a, $b) {
        return $a * $b;
    }

    public static function splitting($a, $b) {
        return $a / $b;
    }


}
